# День рождения А.С. Пушкина - 6 июня
year = int(input('Введите год рождения А.С. Пушкина: '))
if year != 1799:
    print('Неверный год рождения')
else:
    day = input('Введите день рождения А.С. Пушкина: ')
    if day == '6 июня':
        print('Верно')
    else:
        print('Неверный день рождения')
